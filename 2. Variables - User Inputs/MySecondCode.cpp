#include <iostream>
using namespace std;

int main() {
	// C++ Variables
	int LuckyNumber = 207;	// int - stores integers (whole numbers), without decimals, such as 123 or -123
	double decimalLucky = 2.7;	//double - stores floating point numbers, with decimals, such as 19.99 or -19.99
	char favLetter = 'A';	//char - stores single characters, such as 'a' or 'B'. Char values are surrounded by single quotes
	string myName = "Abdul Wadood.";	//string - stores text, such as "Hello World". String values are surrounded by double quotes
	bool myBoolean = true;	//bool - stores values with two states: true or false
	cout << LuckyNumber << " - This is the int value \n";
	cout << decimalLucky << " - This is the double value \n";
	cout << favLetter << " - This is the char value \n";
	cout << myName << " - This is the string value \n";
	cout << myBoolean << " - This is the Boolean value \n";
		
	return 0;
}